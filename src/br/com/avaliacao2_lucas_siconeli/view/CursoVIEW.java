    private void gravar() {
    try {
        cursoDTO.setNome_cur(nome_cur.getText());
        cursoDTO.setDesc_cur(desc_cur.getText());
        cursoDTO.setCod_bar_cur(cod_cur.getText());
        cursoDTO.setP_custo_cur(Double.parseDouble(p_custo_cur.getText()));
        professorDTO.setId_prof(Integer.parseInt(String.valueOf(
                jtl_consultar_professor.getValueAt(
                jtl_consultar_professor.getSelectedRow(), 0))));
        
        JOptionPane.showMessageDialog(null,
                cursoCTR.inserirCurso(cursoDTO, professorDTO));
    } catch (Exception e) {
        System.out.println("Erro ao Gravar: " + e.getMessage());
    }
}
    private void alterar() {
    try {
        cursoDTO.setNome_cur(nome_cur.getText());
        cursoDTO.setDesc_cur(desc_cur.getText());
        cursoDTO.setCod_bar_cur(cod_cur.getText());
        cursoDTO.setP_custo_cur(Double.parseDouble(p_custo_cur.getText()));
        professorDTO.setId_prof(Integer.parseInt(String.valueOf(
                jtl_consultar_professor.getValueAt(
                jtl_consultar_professor.getSelectedRow(), 0))));
        
        JOptionPane.showMessageDialog(null,
                cursoCTR.alterarCurso(cursoDTO, professorDTO));
    } catch (Exception e) {
        System.out.println("Erro ao Gravar: " + e.getMessage());
    }
}
    
    private void excluir(){
    if(JOptionPane.showConfirmDialog(null, "Deseja Realmente excluir o curso?", "Aviso", 
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null,
                    cursoCTR.excluirCurso(cursoDTO));
    }
}
    
