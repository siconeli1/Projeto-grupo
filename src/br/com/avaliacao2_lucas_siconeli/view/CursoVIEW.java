    private void limpaCampos() {
    nome_cur.setText("");
    desc_cur.setText("");
    cod_cur.setText("");
    p_custo_cur.setText("");
    pesquisa_nome_professor.setText("");
    model_jtl_consultar_professor.setNumRows(0);
}
    
    private void preencheTabelacurso(String nome_cur){
    try{
        model_jtl_consultar_curso.setNumRows(0);
        cursoDTO.setNome_cur(nome_cur);
        rs = cursoCTR.consultarCurso(cursoDTO, 1);
        while(rs.next()){
            model_jtl_consultar_curso.addRow(new Object[]{
                rs.getString("id_cur"),
                rs.getString("nome_cur")
            });
        }
    }catch(Exception e){
        System.out.println("Erro ao preencheTabela"+e.getMessage());
    }finally{
        professorCTR.CloseDB();
    }
}

    private void preencheCamposCurso(int id_cur) {
    try {
        cursoDTO.setId_cur(id_cur);
        rs = cursoCTR.consultarCurso(cursoDTO, 2);
        if (rs.next()) {
            limpaCampos();
            nome_cur.setText(rs.getString("nome_cur"));
            desc_cur.setText(rs.getString("desc_cur"));
            cod_cur.setText(rs.getString("cod_bar_cur"));
            p_custo_cur.setText(rs.getString("p_custo_cur"));
            
            
            model_jtl_consultar_professor.setNumRows(0);
            model_jtl_consultar_professor.addRow(new Object[]{rs.getInt("id_prof"), rs.getString("nome_prof"),});
            jtl_consultar_professor.setRowSelectionInterval(0, 0);
            
            gravar_alterar = 2;
            liberaCampos(true);
        }
    } catch (Exception e) {
        System.out.println("Erro ao preencheCamposCurso" + e.getMessage());
    } finally {
        cursoCTR.CloseDB();
    }
}

    private void preencheTabelaprofessor(String nome_prof){
    try{
        model_jtl_consultar_professor.setNumRows(0);
        professorDTO.setNome_prof(nome_prof);
        rs = professorCTR.consultarProfessor(professorDTO, 1);
        while(rs.next()){
            model_jtl_consultar_professor.addRow(new Object[]{
                rs.getString("id_prof"),
                rs.getString("nome_prof")
            });
        }
    }catch(Exception e){
        System.out.println("Erro ao preencheTabelaProfessor"+e.getMessage());
    }finally{
        professorCTR.CloseDB();
    }
}
    
    
    
