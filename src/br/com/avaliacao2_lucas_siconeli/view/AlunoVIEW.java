    private void liberaCampos(boolean a) {
    nome_alu.setEnabled(a);
    logradouro_alu.setEnabled(a);
    numero_alu.setEnabled(a);
    bairro_alu.setEnabled(a);
    cidade_alu.setEnabled(a);
    estado_alu.setEnabled(a);
    cep_alu.setEnabled(a);
    cpf_alu.setEnabled(a);
    rg_alu.setEnabled(a);
}
    private void limpaCampos() {
    nome_alu.setText("");
    logradouro_alu.setText("");
    numero_alu.setText("");
    bairro_alu.setText("");
    cidade_alu.setText("");
    cep_alu.setText("");
    cpf_alu.setText("");
    rg_alu.setText("");
}
    
    private void liberaBotoes(boolean a, boolean b, boolean c, boolean d, boolean e) {
        btnNovo.setEnabled(a);
        btnSalvar.setEnabled(b);
        btnCancelar.setEnabled(c);
        btnExcluir.setEnabled(d);
        btnSair.setEnabled(e);
}
    private void gravar() {
    try {
        alunoDTO.setNome_alu(nome_alu.getText());
        alunoDTO.setLogradouro_alu(logradouro_alu.getText());
        alunoDTO.setNumero_alu(Integer.parseInt(numero_alu.getText()));
        alunoDTO.setBairro_alu(bairro_alu.getText());
        alunoDTO.setCidade_alu(cidade_alu.getText());
        alunoDTO.setEstado_alu(estado_alu.getSelectedItem().toString());
        alunoDTO.setCep_alu(cep_alu.getText());
        alunoDTO.setCpf_alu(cpf_alu.getText());
        alunoDTO.setRg_alu(rg_alu.getText());

        JOptionPane.showMessageDialog(null,
            alunoCTR.inserirAluno(alunoDTO));
    } catch (Exception e) {
        System.out.println("Erro ao gravar: " + e.getMessage());
    }
}
