package br.com.avaliacao2_lucas_siconeli.dto;

public class AlunoDTO {
    private int id_alu, numero_alu;
    private String nome_alu, logradouro_alu, bairro_alu, cidade_alu, estado_alu;
    private String cep_alu, cpf_alu, rg_alu;

    public int getId_alu() {
        return id_alu;
    }

    public void setId_alu(int id_alu) {
        this.id_alu = id_alu;
    }

    public int getNumero_alu() {
        return numero_alu;
    }

    public void setNumero_alu(int numero_alu) {
        this.numero_alu = numero_alu;
    }

    public String getNome_alu() {
        return nome_alu;
    }

    public void setNome_alu(String nome_alu) {
        this.nome_alu = nome_alu;
    }

    public String getLogradouro_alu() {
        return logradouro_alu;
    }

    public void setLogradouro_alu(String logradouro_alu) {
        this.logradouro_alu = logradouro_alu;
    }

    public String getBairro_alu() {
        return bairro_alu;
    }

    public void setBairro_alu(String bairro_alu) {
        this.bairro_alu = bairro_alu;
    }

    public String getCidade_alu() {
        return cidade_alu;
    }

    public void setCidade_alu(String cidade_alu) {
        this.cidade_alu = cidade_alu;
    }

    public String getEstado_alu() {
        return estado_alu;
    }

    public void setEstado_alu(String estado_alu) {
        this.estado_alu = estado_alu;
    }

    public String getCep_alu() {
        return cep_alu;
    }

    public void setCep_alu(String cep_alu) {
        this.cep_alu = cep_alu;
    }

    public String getCpf_alu() {
        return cpf_alu;
    }

    public void setCpf_alu(String cpf_alu) {
        this.cpf_alu = cpf_alu;
    }

    public String getRg_alu() {
        return rg_alu;
    }

    public void setRg_alu(String rg_alu) {
        this.rg_alu = rg_alu;
    }
}
